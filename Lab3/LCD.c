void printMode(int state) {
		char* mode;
		ST7735_FillRect(0, 0, 128, 20, ST7735_Color565(0, 0, 0));

		switch(state) {
				case 0: {
						mode = "CLOCK";
				} break;
				case 1: {
						mode = "SET TIME";
				} break;
				case 2: {
						mode = "ALARM";
				} break;
				case 4: {
					mode = "STOPWATCH";
				} break;
				default: {
						mode = "CLOCK";
				}
		}
		ST7735_SetCursor(7, 0);
		ST7735_OutString(mode);
}

